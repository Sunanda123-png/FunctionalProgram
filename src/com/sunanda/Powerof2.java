package com.sunanda;

public class Powerof2 {
    public static void main(String[] args)
    {
        int n=10;
        int powerof2=1;
        for(int i=1;i<=n;i++)
        {
            powerof2=2*powerof2;
            System.out.println("Result:- "+powerof2 );
        }
        System.out.println("Result:- "+powerof2 );
    }
}
